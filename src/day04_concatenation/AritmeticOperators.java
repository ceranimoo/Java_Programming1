package day04_concatenation;

public class AritmeticOperators {

    public static void main(String[] args) {

        System.out.println(100 +200); // 300

        System.out.println("-----------------");

        System.out.println(40 - 10); // 30

        System.out.println("------------------");

        System.out.println(50 / 6);

        System.out.println( 50 / 6.0 );

        System.out.println( 50.0 / 6);

        System.out.println( 50d / 6);

        System.out.println( (int) (2.5 / 0.5) );

        System.out.println("-------------------");

        System.out.println(10 * 50);

        System.out.println("--------------------");

        System.out.println(1750 % 99);
    }
}
