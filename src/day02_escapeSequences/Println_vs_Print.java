public class Println_vs_Print {


    public static void main(String[] args) {

        System.out.println("Hello everyone!");
        System.out.println("How are you today?"); // This one will give you a space when you print it!

        System.out.print("Hello everyone!");
        System.out.print("how are you today?");
        // This one doesn't give you a space when you print it ! (When you print it it's gonna give you all commend in one line!)


    }
}