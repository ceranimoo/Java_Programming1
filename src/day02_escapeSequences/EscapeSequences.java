package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Students\nToday we have Java Class");// (\n) This sequence adds a new line

        System.out.println("-----------------------------------------");

        System.out.println("\tJava Programming Language");// (\t) This sequence gives you paragraph space
        System.out.println("\t\t\tJava Developer Course Batch 1");

        System.out.println("----------------------------");

        System.out.println("\\"); // (\\) This sequence gives you a SINGLE BACK SLASH.
        // If you wanna print single back slah you have to type (commend) two back slashes.
        System.out.println("-----------------------------");

        System.out.println("My favorite TV show is \"The Walking Dead\"");
        // (\") This sequence gives you quote in your line


    }

}
/*

    1. \n : New Line
    2. \t : Paragraph space
    3. \\ : Single back slash
    4. \" : Double quote

 */